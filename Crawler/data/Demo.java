package com.kunal;

public class Demo {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);

        // to store decimal values
        // produces floating point error
        // for large values, some data will be lost
        float d = 10.45634567876543456789876543234f;
        System.out.println(d);

        // double
        double e = 10.45363;
        System.out.println(e);

        long ll = 23456787543234567l;
        System.out.println(ll);

        boolean bb = true;

        char ch = 'a';

        String s = "Kunal Kushwaha";
        System.out.println(s);
    }
}
